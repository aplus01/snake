import Block from './block.js';

export const buildLevel = (game, level) => {
  const blocks = [];
  level.forEach((row, rowIndex) => {
    row.forEach((block, blockIndex) => {
      if (!block) return;
      blocks.push(
        new Block(
          {
            x: blockIndex * game.blockSize,
            y: rowIndex * game.blockSize + game.blockSize,
          },
          game.blockSize,
          game.blockSize
        )
      );
    });
  });
  return blocks;
};

